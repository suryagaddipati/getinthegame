        <p><dfn><cite>In The Game</cite></dfn> is a documentary film produced by Kartemquin Films with Director Maria Finitzo that follows the ups and downs of an inner city girls&rsquo; soccer team to reveal the very real obstacles that low-income students confront in their quest for higher education.</p>

        <p>Education has always been considered the great equalizer in American society. It is the way less advantaged children improve their potential, and give themselves an opportunity for success and a shot at the American Dream. However, a long-term study recently published reveals that while the achievement gap between white and black students has narrowed significantly over the past few decades, the gap between rich and poor students has grown substantially during the same period. Class is now the major determinant of academic success. An individual&rsquo;s social strata, not only predicts their chances to excel in school, but also affects their opportunities to even attend?</p>

        <p><cite>In the Game</cite> is not a film about the scrappy underdog sports team that through hard work and persistence wins the championship. It is a documentary about how race, class and gender impact the lives and futures of inner city girls. But this is not just a film about loss. Stan is trying to teach the girls about soccer. He&rsquo;d like to see them win a game. But more importantly he is using soccer to teach the girls how to win in life.</p>

